class Jellyfish {
    constructor(
        name = '',
        gender = '',
        tentacles = 15,
        dangerousToHumans = true
    ) {
        this.name = name;
        this.gender = gender;
        this.tentacles = tentacles;
        this.dangerousToHumans = dangerousToHumans;
    }
}

export default Jellyfish;